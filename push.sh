#!/bin/bash
# Скрипт для пуша всех текущих изменений в ветку main

# Переключаемся на main (если не на ней)
git checkout main

# Подтягиваем свежие изменения с удалённого репозитория
git pull origin main --rebase

# Добавляем все изменения (новые, удалённые, изменённые файлы)
git add -A

# Создаём коммит с автоматическим сообщением
git commit -m "Автоматический коммит: $(date '+%Y-%m-%d %H:%M:%S')" || echo "Нет изменений для коммита"

# Пушим в main
git push origin main
